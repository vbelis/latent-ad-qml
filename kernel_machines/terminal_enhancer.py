# Class of colors to be used for displaying colorful things in the terminal,
# to improve the readability of the logs.


class tcols:
    """Class of colors and emotes to be used for displaying colorful things
    in the terminal to improve the readability.
    """

    HEADER = "\033[95m"
    OKBLUE = "\033[94m"
    OKCYAN = "\033[96m"
    OKGREEN = "\033[92m"
    WARNING = "\033[93m"
    FAIL = "\033[91m"
    ENDC = "\033[0m"
    BOLD = "\033[1m"
    UNDERLINE = "\033[4m"
    ROCKET = "\U0001F680"
    SPARKS = "\U00002728"
